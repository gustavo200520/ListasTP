package Lista3.ex01;

public class Aluno {
	private String nome;
	private double nota;
	
	public void setNota(double nota) {
		this.nota = nota;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public Double getNota() {
		return nota;
	}
	
	public String getNome() {
		return nome;
	}
}
